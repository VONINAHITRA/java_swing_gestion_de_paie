private void verifier_pwd(String ref) throws Exception{
    conc = new Connecter_bd();
    char mdp[]=txt_pawd_auth.getPassword();
    for(int i=0;i<mdp.length;i++);
    String query = "SELECT *  FROM utilisateur WHERE nom_user='"+txt_user_auth.getText()+"' AND mot_de_passe_user='"+txt_pawd_auth.getText()+"'";
    ResultSet rs = conc.executeQuery(query);
    if(rs.next()){
     // JOptionPane.showMessageDialog(null," OK mety be","Message",JOptionPane.PLAIN_MESSAGE);    
    progression_ouverutre pouv = new progression_ouverutre();
    pouv.setVisible(true);
    this.dispose();
    
    }else{
    JOptionPane.showMessageDialog(null,"Désolé! Votre mot de passe ou le nom d'utilisateur est incorrect\n Veuillez verifier SVP!","Message",JOptionPane.ERROR_MESSAGE);
     --n;
     txt_info.setText("Attention, Il vous reste " + n + " essai(s)");
     if(n==0){
        this.dispose();
     }
    }
}
