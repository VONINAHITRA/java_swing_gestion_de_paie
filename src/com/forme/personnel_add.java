private void select_perso() throws Exception{
    txt_num_perso.removeAllItems();
    con_db = new Connecter_bd();
    query = "select * from tab_contrat";
    ResultSet rs = con_db.executeQuery(query);
   while(rs.next()){
       String num = rs.getString("num_perso").toString();
       txt_num_perso.addItem(num);
   } 
}
