 public void select_nom(){
    try {
        con_db = new Connecter_bd();
        String num_p = txt_num_perso.getSelectedItem().toString();
        query = "select * from tab_contrat where num_perso='"+num_p+"'";
        ResultSet rs = con_db.executeQuery(query);
        if(rs.next()){
            //String num  = rs.getString("num_perso");   
            String nom = rs.getString("nom_pre_contrat");
            txt_nom_pre_p.setText(nom);
        }
    } catch (Exception ex) {
        Logger.getLogger(personnel_add.class.getName()).log(Level.SEVERE, null, ex);
    }
 }
