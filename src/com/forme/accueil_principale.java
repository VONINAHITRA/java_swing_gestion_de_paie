private void heurescoules() {
        Thread thread = new Thread() {
            public void run() {
                for (;;) {
                    txt_heure.setText(heureActuel());
                    txt_date.setText(dateActuel());
                    try {
                        sleep(1000);
                    } catch (Exception e) {
                        System.out.println("err_1 :" + e.toString());
                    }
                }
            }
        };
        thread.start();
    }

    private String heureActuel() {
        Date date = new Date();
        String hrs = DateFormat.getTimeInstance().format(date);
        return hrs;
    }

    private String dateActuel() {
        Date date = new Date();
        String dt = DateFormat.getDateInstance().format(date);
        return dt;
    }
