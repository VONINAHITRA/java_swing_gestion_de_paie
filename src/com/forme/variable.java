    public void recuperer_val_e() throws Exception {
        tab_var.setRowCount(0);//initialisation du table
        query = "select *  from valeur ";
        st = maCon.Obtenirconnexion().createStatement();
        ResultSet rs = st.executeQuery(query);
        while (rs.next()) {
           String irsa_pourcent = rs.getString("irsa_pourcent");
           String irsa_val =rs.getString("irsa_val");
           String irsa_min =rs.getString("irsa_min");
           String osief_pourcent = rs.getString("osief_pourcent");
           String cnaps_pourcent = rs.getString("cnaps_pourcent");
           String allocation =rs.getString("allocation");
           String supl = rs.getString("supl");
            Object[] var = {irsa_pourcent,irsa_val,irsa_min,osief_pourcent,cnaps_pourcent,allocation,supl};

            tab_var.addRow(var);
        }
    }
    
    
